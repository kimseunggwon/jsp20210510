package ch07;

public class User {
	private String name;
	private String age;
	private String address;
	
	public User(String name, String age, String address) {
		super();
		this.name = name;
		this.age = age;
		this.address = address;
	}
	
	public String getName() {
		return name;
	}
	
	public String getAge() {
		return age;
	}
	
	public String getAddress() {
		return address;
	}

	
	
}
